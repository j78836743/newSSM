public class Bicycle {
    private Integer bicycleId;
    private String bicyclecode;
    private String bicycleName;
    private String publish;
    private Integer bicycleType;
    private Integer bicyclecount;
    private Integer nowcount;
    private String bicycleTypeName;

    public Integer getBicycleId() {
        return bicycleId;
    }

    public void setBicycleId(Integer bicycleId) {
        this.bicycleId = bicycleId;
    }

    public String getBicyclecode() {
        return bicyclecode;
    }

    public void setBicyclecode(String bicyclecode) {
        this.bicyclecode = bicyclecode;
    }

    public String getBicycleName() {
        return bicycleName;
    }

    public void setBicycleName(String bicycleName) {
        this.bicycleName = bicycleName;
    }

    public String getPublish() {
        return publish;
    }

    public void setPublish(String publish) {
        this.publish = publish;
    }

    public Integer getBicycleType() {
        return bicycleType;
    }

    public void setBicycleType(Integer bicycleType) {
        this.bicycleType = bicycleType;
    }

    public Integer getBicyclecount() {
        return bicyclecount;
    }

    public void setBicyclecount(Integer bicyclecount) {
        this.bicyclecount = bicyclecount;
    }

    public Integer getNowcount() {
        return nowcount;
    }

    public void setNowcount(Integer nowcount) {
        this.nowcount = nowcount;
    }

    public String getBicycleTypeName() {
        return bicycleTypeName;
    }

    public void setBicycleTypeName(String bicycleTypeName) {
        this.bicycleTypeName = bicycleTypeName;
    }
}
